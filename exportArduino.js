      "void enqueueNewlyReady(const uint8_t actors[], const uint32_t scores[], uint8_t count) {",
      "  bool used[ACTOR_COUNT] = { false, false, false, false };",
      "  for (uint8_t output = 0; output < count && readyCount < ACTOR_COUNT; output++) {",
      "    int8_t best = -1;",
      "    for (uint8_t candidate = 0; candidate < count; candidate++) {",
      "      if (used[candidate]) continue;",
      "      if (best < 0 || scores[candidate] > scores[(uint8_t)best] ||",
      "          (scores[candidate] == scores[(uint8_t)best] && actors[candidate] < actors[(uint8_t)best])) {",
      "        best = candidate;",
      "      }",
      "    }",
      "    if (best >= 0) {",
      "      used[(uint8_t)best] = true;",
      "      readyQueue[readyCount++] = actors[(uint8_t)best];",
      "    }",
      "  }",
      "}",
