  function numberList(values) {
    return values.map(function (value) {
      return String(Math.max(0, Math.round(Number(value) || 0)));
    }).join(", ");
  }

  function numberLines(values, perLine) {
    var lines = [];

    for (var i = 0; i < values.length; i += perLine) {
      lines.push("  " + numberList(values.slice(i, i + perLine)) + (i + perLine < values.length ? "," : ""));
    }

    return lines.join("\n");
  }

  function midiToHz(value) {
    var midi = Number(value) || 0;

    if (midi <= 0) {
      return 0;
    }

    return Math.max(1, Math.min(20000, Math.round(440 * Math.pow(2, (midi - 69) / 12))));
  }

  function hzToHalfPeriodUs(hz) {
    var safeHz = Number(hz) || 0;

    if (safeHz <= 0) {
      return 0;
    }

    return Math.max(1, Math.min(65535, Math.round(500000 / safeHz)));
  }

  function assignedMusicSongs(project) {
    var songs = Array.isArray(project && project.music) ? project.music : [];
    var assignments = project && project.settings && project.settings.musicAssignments ?
      project.settings.musicAssignments :
      {};
    var result = [];

    function findSong(id) {
      return songs.find(function (song) {
        return song && song.id === id;
      }) || null;
    }

    function addSong(song, role) {
      if (!song || result.some(function (entry) { return entry.song === song; })) {
        return;
      }
      result.push({
        song: song,
        role: role
      });
    }

    addSong(findSong(assignments.worldmapMusicId), "Worldmap");
    addSong(findSong(assignments.battleMusicId), "Battle");

    songs.forEach(function (song) {
      if (result.length < 2) {
        addSong(song, result.length === 0 ? "Worldmap" : "Battle");
      }
    });

    return result.slice(0, 2);
  }

  function songGatePercent(song) {
    var gate = Number(song && song.settings && song.settings.gate);

    if (!Number.isFinite(gate) || gate <= 0) {
      gate = 0.82;
    }
    if (gate <= 1) {
      gate *= 100;
    }

    return Math.max(20, Math.min(100, Math.round(gate)));
  }

  function songBpm(song) {
    var bpm = Number(song && song.settings && song.settings.bpm);

    return Math.max(40, Math.min(240, Math.round(Number.isFinite(bpm) && bpm > 0 ? bpm : 120)));
  }

  function songChannels(song) {
    return song && song.channels && typeof song.channels === "object" ? song.channels : {};
  }

  function songEventCount(song) {
    var channels = songChannels(song);
    var melody = Array.isArray(channels.melodyNotes) ? channels.melodyNotes : [];
    var durations = Array.isArray(channels.durations) ? channels.durations : [];

    return Math.min(192, melody.length, durations.length);
  }

