  function segmentByteForMapChar(c) {
    switch (c) {
      case "#": return 0x7F;
      case "0": return 0x7E;
      case "1": return 0x30;
      case "2": return 0x6D;
      case "3": return 0x79;
      case "4": return 0x33;
      case "5": return 0x5B;
      case "6": return 0x5F;
      case "7": return 0x70;
      case "8": return 0x7F;
      case "9": return 0x7B;
      case "-": return 0x01;
      case ".": return 0x00;
      case " ": return 0x00;
      default: return 0x00;
    }
  }

  function compressedPalette(project) {
    var palette = [" ", ".", "#"];
    var seen = { " ": true, ".": true, "#": true };

    for (var i = 0; i < project.rooms.length; i += 1) {
      var frame = normalizedRoomFrame(project.rooms[i]);

      for (var index = 0; index < frame.length; index += 1) {
        var c = frame.charAt(index);

        if (!seen[c]) {
          seen[c] = true;
          palette.push(c);
        }
      }
    }

    while (palette.length < 8) {
      palette.push(" ");
    }

    return palette.slice(0, 8);
  }

  function paletteIndexMap(palette) {
    var map = {};

    for (var i = 0; i < palette.length; i += 1) {
      if (map[palette[i]] === undefined) {
        map[palette[i]] = i;
      }
    }

    return map;
  }

  function packFrame3Bit(frame, indexByChar) {
    var bytes = [];

    for (var b = 0; b < 9; b += 1) {
      bytes.push(0);
    }

    for (var cell = 0; cell < 24; cell += 1) {
      var glyph = indexByChar[frame.charAt(cell)];

      if (glyph === undefined) {
        glyph = 0;
      }

      var bitOffset = cell * 3;
      var byteIndex = Math.floor(bitOffset / 8);
      var shift = bitOffset % 8;

      bytes[byteIndex] |= (glyph << shift) & 0xFF;
      if (shift > 5) {
        bytes[byteIndex + 1] |= glyph >> (8 - shift);
      }
    }

    return bytes;
  }

  function byteList(values) {
    return values.map(function (value) {
      return "0x" + value.toString(16).toUpperCase().padStart(2, "0");
    }).join(", ");
  }

  function byteLines(values, perLine) {
    var lines = [];

    for (var i = 0; i < values.length; i += perLine) {
      lines.push("  " + byteList(values.slice(i, i + perLine)) + (i + perLine < values.length ? "," : ""));
    }

    return lines.join("\n");
  }

