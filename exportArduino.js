      "",
      "uint16_t audioSongEventOffset(uint8_t songIndex) { return pgm_read_word(&audioSongOffset[songIndex]); }",
      "uint16_t audioSongEventLength(uint8_t songIndex) { return pgm_read_word(&audioSongLength[songIndex]); }",
      "uint16_t activeAudioSongBpm() { return pgm_read_word(&audioSongBpm[currentMusicSong]); }",
      "uint8_t activeAudioSongGate() { return pgm_read_byte(&audioSongGatePercent[currentMusicSong]); }",
