      "  uint8_t hitChance = battleHitChance(attackerHero, defenderHero);",
      "  uint8_t roll;",
      "  bool hit;",
      "  bool critical;",
      "  int16_t damage;",
      "  if (BATTLE_CALC_METHOD == 0) {",
      "    int16_t attackBonus = power / 4 + actorLevel(attackerHero) / 4;",
      "    int16_t armorClass = 10 + defense / 4;",
      "    roll = rollDie(20);",
      "    critical = roll == 20 || percentRoll() <= actorCriticalChance(attackerHero);",
      "    hit = roll == 20 || (roll != 1 && (int16_t)roll + attackBonus >= armorClass);",
      "    damage = (int16_t)power + rollDie(8);",
      "    return finalBattleDamage(hit, critical ? damage * 2 : damage, defenderHero);",
      "  }",
      "  if (BATTLE_CALC_METHOD == 1) {",
      "    int16_t attackBonus = actorLevel(attackerHero) + power / 4;",
      "    int16_t defenseNumber = 10 + actorLevel(defenderHero) + defense / 4;",
      "    roll = rollDie(20);",
      "    int16_t total = roll + attackBonus;",
      "    hit = roll == 20 || (roll != 1 && total >= defenseNumber);",
      "    critical = hit && (roll == 20 || total >= defenseNumber + 10);",
      "    damage = (int16_t)power + rollDie(6);",
      "    return finalBattleDamage(hit, critical ? damage * 2 : damage, defenderHero);",
      "  }",
      "  if (BATTLE_CALC_METHOD == 2) {",
      "    uint8_t skill = 8 + power / 10;",
      "    uint8_t defend = 8 + actorEvasion(defenderHero) / 20;",
      "    uint8_t attackRoll = rollDie(6) + rollDie(6) + rollDie(6);",
      "    uint8_t defendRoll = rollDie(6) + rollDie(6) + rollDie(6);",
      "    hit = attackRoll <= skill && defendRoll > defend;",
      "    damage = (int16_t)rollDie(6) + power / 5 - defense / 3;",
      "    return finalBattleDamage(hit, damage, defenderHero);",
      "  }",
      "  if (BATTLE_CALC_METHOD == 3) {",
      "    int8_t attackSkill = power / 25;",
      "    int8_t defendSkill = defense / 25 + actorEvasion(defenderHero) / 25;",
      "    int8_t shifts = attackSkill + fateRoll() - defendSkill - fateRoll();",
      "    damage = shifts + actorWeaponPower(attackerHero) / 25;",
      "    return finalBattleDamage(shifts > 0, damage, defenderHero);",
      "  }",
      "  if (BATTLE_CALC_METHOD == 4) {",
      "    uint8_t skill = actorAccuracy(attackerHero);",
      "    uint8_t dodge = actorEvasion(defenderHero);",
      "    uint8_t attackRoll = percentRoll();",
      "    uint8_t defendRoll = percentRoll();",
      "    uint8_t attackLevel = attackRoll <= skill / 5 ? 3 : attackRoll <= skill / 2 ? 2 : attackRoll <= skill ? 1 : 0;",
      "    uint8_t defendLevel = defendRoll <= dodge / 5 ? 3 : defendRoll <= dodge / 2 ? 2 : defendRoll <= dodge ? 1 : 0;",
      "    hit = attackLevel > 0 && attackLevel >= defendLevel;",
      "    damage = (int16_t)rollDie(8) + power / 5 - defense / 5;",
      "    return finalBattleDamage(hit, attackLevel == 3 ? damage * 2 : damage, defenderHero);",
      "  }",
      "  if (BATTLE_CALC_METHOD == 5 || BATTLE_CALC_METHOD == 6) {",
      "    uint8_t sides = BATTLE_CALC_METHOD == 5 ? 6 : 10;",
      "    uint8_t pool = 1 + power / 20;",
      "    uint8_t defendPool = 1 + (actorEvasion(defenderHero) + defense) / 30;",
      "    uint8_t hits = 0;",
      "    uint8_t blocks = 0;",
      "    uint8_t target = (sides * 2 + 2) / 3;",
      "    for (uint8_t i = 0; i < pool; i++) if (rollDie(sides) >= target) hits++;",
      "    for (uint8_t i = 0; i < defendPool; i++) if (rollDie(sides) >= target) blocks++;",
      "    if (hits <= blocks) return 0;",
      "    uint8_t base = BATTLE_CALC_METHOD == 5 ? 2 + power / 12 : 1 + power / 25;",
      "    return finalBattleDamage(true, (int16_t)base + hits - blocks, defenderHero);",
      "  }",
      "  if (BATTLE_CALC_METHOD == 7) {",
      "    if (percentRoll() > hitChance) return 0;",
      "    uint8_t hits = 1 + actorLevel(attackerHero) / 16;",
      "    uint8_t high = power > 0 ? power * 2 : 1;",
      "    damage = (int16_t)(randomRange(power, high)) - defense;",
      "    damage *= hits;",
      "    if (percentRoll() <= actorCriticalChance(attackerHero)) damage += power;",
      "    return finalBattleDamage(true, damage, defenderHero);",
      "  }",
      "  if (BATTLE_CALC_METHOD == 8) {",
      "    if (percentRoll() > hitChance) return 0;",
      "    int16_t adjusted = (int16_t)power / 2 - (int16_t)defense / 4;",
      "    if (adjusted < 1) adjusted = 1;",
      "    critical = percentRoll() <= actorCriticalChance(attackerHero);",
      "    if (critical) return finalBattleDamage(true, power, defenderHero);",
      "    return finalBattleDamage(true, randomRange((uint8_t)((adjusted * 3 + 3) / 4), (uint8_t)((adjusted * 5 + 3) / 4)), defenderHero);",
      "  }",
      "  if (BATTLE_CALC_METHOD == 9) {",
      "    if (percentRoll() > hitChance) return 0;",
      "    uint8_t attackStat = power < 1 ? 1 : power;",
      "    uint8_t defenseStat = defense < 1 ? 1 : defense;",
      "    uint32_t base = (((uint32_t)(2 * actorLevel(attackerHero) / 5 + 2) * power * attackStat) / defenseStat) / 50 + 2;",
      "    base = base * randomRange(85, 100) / 100;",
      "    if (percentRoll() <= actorCriticalChance(attackerHero)) base = base * 3 / 2;",
      "    return finalBattleDamage(true, base > 99 ? 99 : (int16_t)base, defenderHero);",
      "  }",
      "  if (percentRoll() > hitChance) return 0;",
      "  damage = (int16_t)power + randomRange(0, power) - defense;",
      "  return finalBattleDamage(true, damage, defenderHero);",
      "}",
