  function cStringLiteral(text) {
    return String(text || "")
      .replace(/\\/g, "\\\\")
      .replace(/"/g, '\\"')
      .replace(/\r/g, "\\r")
      .replace(/\n/g, "\\n");
  }

  function hexByte(value) {
    return "0x" + Math.max(0, Math.min(255, Math.round(Number(value) || 0)))
      .toString(16)
      .toUpperCase()
      .padStart(2, "0");
  }

