  function preflightCombinedGameProject(project) {
    if (!window.SevenSegPreflight || typeof window.SevenSegPreflight.run !== "function") {
      return {
        status: "block",
        blockers: [{
          code: "PREFLIGHT_UNAVAILABLE",
          message: "Combined-game preflight is unavailable."
        }],
        warnings: []
      };
    }

    return window.SevenSegPreflight.run(
      project || window.SevenSegModel.defaultProject,
      { finalExport: false }
    );
  }

  function assertCombinedGameExportable(project) {
    var report = preflightCombinedGameProject(project);

    if (report.blockers.length) {
      var error = new Error(
        "Combined export blocked: " +
        report.blockers.slice(0, 3).map(function (entry) {
          return entry.message;
        }).join(" ")
      );
      error.name = "SevenSegPreflightError";
      error.preflightReport = report;
      throw error;
    }

    return report;
  }

  var arduinoTestsDirectoryHandle = null;

  function padTimestampPart(value, width) {
    return String(value).padStart(width, "0");
  }

  function arduinoTimestamp(date) {
    return [
      date.getFullYear(),
      padTimestampPart(date.getMonth() + 1, 2),
      padTimestampPart(date.getDate(), 2),
      "_",
      padTimestampPart(date.getHours(), 2),
      padTimestampPart(date.getMinutes(), 2),
      padTimestampPart(date.getSeconds(), 2),
      "_",
      padTimestampPart(date.getMilliseconds(), 3)
    ].join("");
  }

  function safeSketchBaseName(name) {
    var baseName = String(name || "SevenSeg_Test")
      .replace(/\.ino$/i, "")
      .replace(/[^A-Za-z0-9_]/g, "_")
      .replace(/_+/g, "_")
      .replace(/^_+|_+$/g, "");

    if (!baseName || !/^[A-Za-z]/.test(baseName)) {
      baseName = "SevenSeg_" + baseName;
    }

    return baseName;
  }

  function downloadIno(filename, code) {
    var blob = new Blob([code], { type: "text/plain" });
    var url = URL.createObjectURL(blob);
    var link = document.createElement("a");

    link.href = url;
    link.download = filename;
    document.body.appendChild(link);
    link.click();
    link.remove();
    URL.revokeObjectURL(url);
  }

  async function getArduinoTestsDirectory() {
    if (arduinoTestsDirectoryHandle) {
      return arduinoTestsDirectoryHandle;
    }

    var selectedDirectory = await window.showDirectoryPicker({
      id: "sevenseg-arduino-tests",
      mode: "readwrite"
    });

    if (selectedDirectory.name === "Arduino_Tests") {
      arduinoTestsDirectoryHandle = selectedDirectory;
    } else {
      arduinoTestsDirectoryHandle = await selectedDirectory.getDirectoryHandle("Arduino_Tests", {
        create: true
      });
    }

    return arduinoTestsDirectoryHandle;
  }

  async function exportIno(sketchBaseName, code) {
    var baseName = safeSketchBaseName(sketchBaseName);
    var sketchName = baseName + "_" + arduinoTimestamp(new Date());
    var filename = sketchName + ".ino";

    if (typeof window.showDirectoryPicker === "function") {
      try {
        var arduinoTestsDirectory = await getArduinoTestsDirectory();
        var sketchDirectory = await arduinoTestsDirectory.getDirectoryHandle(sketchName, {
          create: true
        });
        var sketchFile = await sketchDirectory.getFileHandle(filename, {
          create: true
        });
        var sketchWriter = await sketchFile.createWritable();

        await sketchWriter.write(code);
        await sketchWriter.close();

        var markerFile = await arduinoTestsDirectory.getFileHandle("LATEST_" + baseName + ".txt", {
          create: true
        });
        var markerWriter = await markerFile.createWritable();

        await markerWriter.write(
          "Latest export: " + sketchName + "\n" +
          "Sketch: " + sketchName + "/" + filename + "\n"
        );
        await markerWriter.close();

        return {
          canceled: false,
          savedToFolder: true,
          sketchName: sketchName,
          filename: filename,
          relativePath: "Arduino_Tests/" + sketchName + "/" + filename
        };
      } catch (error) {
        if (error && error.name === "AbortError") {
          return {
            canceled: true,
            savedToFolder: false,
            sketchName: sketchName,
            filename: filename
          };
        }

        arduinoTestsDirectoryHandle = null;
      }
    }

    downloadIno(filename, code);
    return {
      canceled: false,
      savedToFolder: false,
      sketchName: sketchName,
      filename: filename,
      relativePath: filename
    };
  }

