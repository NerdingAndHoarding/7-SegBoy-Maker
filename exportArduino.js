      "void updateActorTimers() {",
      "  if (screenMode != MODE_TIMER) return;",
      "  unsigned long now = millis();",
      "  unsigned long elapsed = now - lastTimerTick;",
      "  if (elapsed == 0) return;",
      "  lastTimerTick = now;",
      "",
      "  uint8_t newlyReady[ACTOR_COUNT];",
      "  uint32_t readyScore[ACTOR_COUNT];",
      "  uint8_t newlyReadyCount = 0;",
      "",
      "  for (uint8_t actorIndex = 0; actorIndex < ACTOR_COUNT; actorIndex++) {",
      "    if (!actorIsAlive(actorIndex) || actorIsQueued(actorIndex)) continue;",
      "    uint32_t nextTimer = (uint32_t)actorTimer[actorIndex] + elapsed * actorSwiftness[actorIndex];",
      "    actorTimer[actorIndex] = nextTimer >= TIMER_FULL ? TIMER_FULL : (uint16_t)nextTimer;",
      "    if (actorTimer[actorIndex] >= TIMER_FULL) {",
      "      newlyReady[newlyReadyCount] = actorIndex;",
      "      readyScore[newlyReadyCount] = nextTimer;",
      "      newlyReadyCount++;",
      "    }",
      "  }",
      "",
      "  if (newlyReadyCount > 0) {",
      "    enqueueNewlyReady(newlyReady, readyScore, newlyReadyCount);",
      "    startNextReadyActor();",
      "    return;",
      "  }",
      "",
      "  if (now - lastChargeRender >= CHARGE_RENDER_MS) {",
      "    lastChargeRender = now;",
      "    renderCharging();",
      "  }",
      "}",
