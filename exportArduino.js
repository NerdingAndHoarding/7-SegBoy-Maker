      "      renderMessage(\"DEAD\");",
      "      delay(700);",
      "      renderTargetSelect();",
      "      return;",
      "    }",
      "    if (currentActorIndex == 0 && selectedTargetRow == 1) {",
      "      uint8_t pathCells[4] = { 5, 4, 10, 9 };",
      "      uint8_t pathSegments[4] = { 0x01, 0x08, 0x40, 0x01 };",
      "      for (uint8_t i = 0; i < 4; i++) {",
      "        renderProjectileStep(pathCells[i], pathSegments[i], i < 2);",
      "        delay(110);",
      "      }",
      "    } else {",
      "      for (int8_t projectileX = 5; projectileX >= 2; projectileX--) {",
      "        renderProjectileStep(selectedTargetRow * 8 + (uint8_t)projectileX, segmentForChar('-'), projectileX >= 4);",
      "        delay(110);",
      "      }",
      "    }",
      "    char damageText[4];",
      "    makeDamageText(damageText, PLAYER_ATTACK);",
      "    renderBattleEffect(selectedTargetRow, damageText, 2, selectedTargetRow, false);",
      "    delay(170);",
      "    renderBattleEffect(selectedTargetRow, damageText, 2, -1, false);",
      "    delay(280);",
      "    int8_t oldHp = foeHp[selectedTargetRow];",
      "    foeHp[selectedTargetRow] -= PLAYER_ATTACK;",
      "    if (foeHp[selectedTargetRow] < 0) foeHp[selectedTargetRow] = 0;",
      "    if (oldHp > 0 && foeHp[selectedTargetRow] <= 0) {",
      "      blinkDefeated(selectedTargetRow);",
      "    }",
      "  } else {",
      "    if (!targetIsSelectable(pendingAction, selectedTargetRow)) {",
      "      renderMessage(\"DEAD\");",
      "      delay(700);",
      "      renderTargetSelect();",
      "      return;",
      "    }",
      "    int16_t missingHp = 99 - (int16_t)partyHp[selectedTargetRow];",
      "    if (missingHp < 0) missingHp = 0;",
      "    uint8_t appliedHeal = PLAYER_HEAL > (uint8_t)missingHp ? (uint8_t)missingHp : PLAYER_HEAL;",
      "    char healText[3];",
      "    makeUnsignedText(healText, appliedHeal);",
      "    renderBattleEffect(selectedTargetRow, healText, 4, 2 + selectedTargetRow, true);",
      "    delay(170);",
      "    renderBattleEffect(selectedTargetRow, healText, 4, -1, true);",
      "    delay(280);",
      "    partyHp[selectedTargetRow] += appliedHeal;",
      "  }",
      "",
      "  completeCurrentActorAction();",
      "}",
