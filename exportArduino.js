  function normalizedRoomFrame(room) {
    var model = window.SevenSegModel;
    var source = room && room.data && typeof room.data.pocV1Frame === "string" ?
      room.data.pocV1Frame :
      room.frame || "";
    var frame = model.normalizeFrame24(source);
    var clean = "";

    for (var i = 0; i < model.DISPLAY_CELLS; i += 1) {
      var c = frame.charAt(i);
      clean += c === "#" || c === "8" || c === "." ? c : " ";
    }

    return clean;
  }

