  function compressedWorldDimensions(project) {
    var width = 1;
    var height = 1;

    for (var i = 0; i < project.rooms.length; i += 1) {
      width = Math.max(width, Number(project.rooms[i].x) + 1);
      height = Math.max(height, Number(project.rooms[i].y) + 1);
    }

    return {
      width: Math.max(1, Math.min(window.SevenSegModel.ROOM_X_COUNT, width)),
      height: Math.max(1, Math.min(window.SevenSegModel.ROOM_Y_MAX - window.SevenSegModel.ROOM_Y_MIN + 1, height))
    };
  }

  function buildCompressedWorld(project) {
    var model = window.SevenSegModel;
    var dimensions = compressedWorldDimensions(project);
    var palette = compressedPalette(project);
    var indexByChar = paletteIndexMap(palette);
    var roomsByKey = {};
    var packed = [];
    var blockFlags = 0;

    for (var p = 0; p < palette.length; p += 1) {
      if (palette[p] === "#" || palette[p] === "8") {
        blockFlags |= (1 << p);
      }
    }

    for (var i = 0; i < project.rooms.length; i += 1) {
      roomsByKey[project.rooms[i].x + "," + project.rooms[i].y] = normalizedRoomFrame(project.rooms[i]);
    }

    for (var y = 0; y < dimensions.height; y += 1) {
      for (var x = 0; x < dimensions.width; x += 1) {
        var key = x + "," + y;
        var frame = roomsByKey[key] || model.normalizeFrame24("");
        packed = packed.concat(packFrame3Bit(frame, indexByChar));
      }
    }

    return {
      width: dimensions.width,
      height: dimensions.height,
      roomCount: dimensions.width * dimensions.height,
      bytesPerRoom: 9,
      paletteSegments: palette.map(segmentByteForMapChar),
      paletteBlockFlags: blockFlags,
      packedRoomBytes: packed
    };
  }

